#!/usr/bin/env python3
"""
3-to_str.py

A simple string conversion module that provides a to_str function.

This module is used to convert numbers to their string representations.

Author: Malik Hussein
"""


def to_str(n: float) -> str:
    """
    Converts a float number to a string.

    Args:
        n (float): The number to convert.

    Returns:
        str: The string representation of the number.

    Example:
        >>> to_str(3.14)
        '3.14'
    """
    return str(n)
